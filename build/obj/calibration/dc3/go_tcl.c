

char go_tcl_proc[]=
"#!/usr/bin/wish -f\n"
"\n"
"#\n"
"# This file is a script for automating the dc calibration process.\n"
"# It basically pushes all of the buttons in the sequence that a calibrator\n"
"# would push them.\n"
"#\n"
"# The proc \"invokeButton\" was written below to help give the user a\n"
"# more visual aid as to what is going on.\n"
"#\n"
"# Useful commands:\n"
"#\n"
"# 	To raise a notebook page-\n"
"#\n"
"#      Notebook:raise.page $notebookwin 0  # For T0/Tmax\n"
"#      Notebook:raise.page $notebookwin 1  # For Fit\n"
"#      Notebook:raise.page $notebookwin 2  # For Calibration Quality\n"
"#      Notebook:raise.page $notebookwin 3  # For Database\n"
"#\n"
"# 	To push a button-\n"
"#\n"
"#      invokeButton $find_tmax_win.tmax.find.findall	# Find All Tmax values \n"
"#      invokeButton $find_tmax_win.tzero.find.findall	# Find All Tzero values \n"
"#      invokeButton $fit_path.novice.fit.fitall			# Fit all X vs. T parms \n"
"#      invokeButton $quality_path.left.test				# Quality Summary \n"
"#      invokeButton $map_path.controls.map				# Write to Database\n"
"#\n"
"#\n"
"#\n"
"# 	Other useful tidbits-\n"
"#\n"
"#      if {[$summary_win_exists!=0]} {invokeButton .qualsum.b.save}	# \"Save as postscript...\"\n"
"#      if {[$summary_win_exists!=0]} {invokeButton .qualsum.b.print}	# \"Print...\"\n"
"#\n"
"#      ModifyDBInfo							# Bring up the dialog to edit DB information\n"
"#      tkwait window $changedbinfowin	# Wait for the user to finish editing DB info\n"
"#      set seek_first 1						# Turn on \"seek first\" option\n"
"#      set seek_first 0						# Turn off \"seek first\" option\n"
"#\n"
"#\n"
"#\n"
"\n"
"proc go {} {\n"
"\n"
"	global find_tmax_win fit_path quality_path summary_win_exists changedbinfowin\n"
"	global map_path seek_first\n"
"	global script_ready\n"
"\n"
"	set notebookwin .middle.n\n"
"\n"
"	# Diable the GO button while we work\n"
"	.top.go configure -state disabled\n"
"\n"
"	# Have the screen update more frequently while we do this\n"
"	# (gives the illusion that fewer things are going wrong)\n"
"	SetReplotGlobal\n"
"\n"
"	####################### Start Doing Stuff ########################\n"
"	# Find All Tmax values\n"
"	Notebook:raise.page $notebookwin 0 ; update ; update\n"
"	invokeButton $find_tmax_win.tmax.find.findall\n"
"\n"
"	# Fit X vs. T function(s)\n"
"	Notebook:raise.page $notebookwin 1 ; update ; update\n"
"	set seek_first 1\n"
"	invokeButton $fit_path.novice.fit.fitall\n"
"\n"
"\n"
"	####################### Finished Doing Stuff ########################\n"
"\n"
"	# Stop replot loop\n"
"	after cancel after idle SetReplotGlobal\n"
"	after cancel SetReplotGlobal\n"
"\n"
"	# Enable the GO button\n"
"	.top.go configure -state normal\n"
"}\n"
"\n"
"\n"
"\n"
"#\n"
"# Utility to invoke a button. This will highlight the button (as\n"
"# with mouseovers), and flash it before invoking it. After the\n"
"# invocation is complete, the button is flashed again and set back\n"
"# to the normal state.\n"
"#\n"
"proc invokeButton {b} {\n"
"\n"
"	update ; update\n"
"	$b configure -state active\n"
"	$b flash\n"
"	$b invoke\n"
"	$b flash\n"
"	$b configure -state normal\n"
"	update ; update	\n"
"}\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"

;

