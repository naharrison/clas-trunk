

char options_tcl_proc[]=
"#!/usr/bin/wish -f\n"
"\n"
"set showplots 1\n"
"set show_function 1\n"
"set show_scatter 1\n"
"set show_profile 1\n"
"set show_predicted_resi 1\n"
"set show_zero_line 1\n"
"\n"
"set show_residual 1\n"
"set use_corrected_time 1\n"
"\n"
"proc InitOptions {path} {\n"
"\n"
"   global options_path\n"
"   global expert\n"
"   global showplots show_function show_scatter show_profile\n"
"   global show_predicted_resi show_zero_line\n"
"   global recalc_residuals recalc_method\n"
"   global use_corrected_time\n"
"\n"
"   set options_path $path\n"
"   \n"
"   checkbutton $path.expert -text \"Expert Mode\" -variable expert -anchor w \n"
"   checkbutton $path.plot -text \"Show Plots\" -variable showplots -anchor w\n"
"   pack $path.expert $path.plot -side top -fill x\n"
"   menubutton $path.plotmenu -relief groove -text \"Plot >\" -menu $path.plotmenu.m\n"
"   pack $path.plotmenu -side top\n"
"   frame $path.plottype\n"
"   radiobutton $path.plottype.resi -text \"Resi\" -variable show_residual -value 1\n"
"   radiobutton $path.plottype.xvst -text \"XvsT\" -variable show_residual -value 0\n"
"   pack $path.plottype.resi $path.plottype.xvst -side left -fill both\n"
"   checkbutton $path.recalc -text \"Re-calc. Residuals\" -variable recalc_residuals\n"
"   frame $path.recalcmethod\n"
"   radiobutton $path.recalcmethod.fct -text \"Function\" -variable recalc_method -value 0\n"
"   radiobutton $path.recalcmethod.table -text \"Table\" -variable recalc_method -value 1\n"
"   pack $path.recalcmethod.fct $path.recalcmethod.table -side left -fill both\n"
"   checkbutton $path.corrt -text \"Corrected t-axis\" -variable use_corrected_time -anchor w \n"
"   pack $path.corrt -side top -fill x\n"
"   button $path.aec -text \"Additional Expert\nControls\" \\n"
"   	-command {OpenExpertControls}\n"
"   \n"
"   ########## Make Plot menu ######################################\n"
"   set w $path.plotmenu.m\n"
"   menu $w\n"
"      $w add checkbutton -label \"Function\" -variable show_function\n"
"      $w add checkbutton -label \"Scatter\" -variable show_scatter\n"
"      $w add checkbutton -label \"Profile\" -variable show_profile\n"
"      $w add checkbutton -label \"Zero Line\" -variable show_zero_line\n"
"      $w add checkbutton -label \"Predicted Resi\" -variable show_predicted_resi\n"
"\n"
"   ######### disable/enable items by tracing variables ###########\n"
"   trace variable showplots w {UpdatePlotsMenuState}\n"
"   trace variable recalc_residuals w {UpdateRecalcMethodState}\n"
"\n"
"}\n"
"\n"
"proc UpdatePlotsMenuState {name1 name2 op} {\n"
"   \n"
"   global options_path showplots\n"
"   \n"
"   if {[expr $showplots==0]} {\n"
"      $options_path.plotmenu configure -state disabled \\n"
"   } else {\n"
"      $options_path.plotmenu configure -state normal \\n"
"   }\n"
"\n"
"}\n"
"\n"
"proc UpdateRecalcMethodState {name1 name2 op} {\n"
"   \n"
"   global options_path recalc_residuals\n"
"   \n"
"   if {[expr $recalc_residuals==0]} {\n"
"      $options_path.recalcmethod.fct configure -state disabled ;\\n"
"      $options_path.recalcmethod.table configure -state disabled ;\\n"
"   } else {\n"
"      $options_path.recalcmethod.fct configure -state normal ;\\n"
"      $options_path.recalcmethod.table configure -state normal ;\\n"
"   }\n"
"\n"
"}\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"

;

