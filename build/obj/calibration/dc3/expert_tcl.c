

char expert_tcl_proc[]=
"#!/usr/bin/wish -f\n"
"\n"
"\n"
"# The routines in here unpack/pack and disable/enable\n"
"# widgets to bring them in and out of \"expert\" mode\n"
"\n"
"global expert\n"
"\n"
"\n"
"\n"
"# Trace the \"expert\" variable that is changed whenever\n"
"# the \"Expert Mode\" checkbutton is toggled\n"
"trace variable expert w {ExpertToggle}\n"
"\n"
"# The routine called when \"expert\" is changed. This just\n"
"# calls either \"ExpertOn\" or \"ExpertOff\" depending on the\n"
"# value of \"expert\".\n"
"proc ExpertToggle {name1 name2 ops} {\n"
"   global expert\n"
"   \n"
"   if {[expr $expert == 0]} then {ExpertOff} else {ExpertOn}\n"
"}\n"
"\n"
"# Pack and enable all \"expert\" features\n"
"proc ExpertOn {} {\n"
"   \n"
"   global find_tmax_win options_path fit_path\n"
"\n"
"   # Warn user about expert mode\n"
"   ExpertWarn\n"
"   \n"
"   set state normal\n"
"   \n"
"   # Main window\n"
"   #pack .middle.left.secsup.sec.sec0 .middle.left.secsup.sup.sup0 -side top -fill x\n"
"   pack $options_path.plottype -side top -fill both\n"
"   pack $options_path.recalc -side top -fill x\n"
"   pack $options_path.recalcmethod -side top -fill x\n"
"   pack $options_path.corrt -side top -fill x\n"
"	pack $options_path.aec -side top\n"
"\n"
"   # Tmax/Tzero\n"
"   pack $find_tmax_win.tzero -fill x -side top\n"
"\n"
"   # Fit\n"
"   pack $fit_path.bottom.expert\n"
"\n"
"}\n"
"\n"
"# Unpack and disable all \"expert features\"\n"
"proc ExpertOff {} {\n"
"\n"
"   global sec sup\n"
"   global find_tmax_win options_path fit_path show_residual\n"
"\n"
"   set state disabled\n"
"\n"
"   # Main window\n"
"	#pack forget .middle.left.secsup.sec.sec0 .middle.left.secsup.sup.sup0\n"
"   if {[expr $sec==0]} then {set sec 1}\n"
"   if {[expr $sup==0]} then {set sup 1}\n"
"   if {[expr $show_residual==0]} then {set show_residual 1}\n"
"   pack forget $options_path.plottype\n"
"   pack forget $options_path.recalc\n"
"   pack forget $options_path.recalcmethod\n"
"   pack forget $options_path.corrt -side top -fill x\n"
"	pack forget $options_path.aec -side top\n"
"\n"
"   # Tmax/Tzero\n"
"   pack forget $find_tmax_win.tzero\n"
"\n"
"   # Fit\n"
"   pack forget $fit_path.bottom.expert\n"
"\n"
"	# Expert Controls Window\n"
"	CloseExpertControls\n"
"}\n"
"\n"
"proc ExpertWarn {} {\n"
"\n"
"   tk_dialog .expertwarn \"Expert Mode\" \\n"
"\"Warning: Any changes made while in expert mode will\n"
"require a password before allowing you to write\n"
"to the Map.\" \\n"
"   warning 0 OK \n"
"\n"
"}\n"
"\n"
"\n"
"# Disable all expert features at startup\n"
"ExpertToggle \"\" \"\" \"\"\n"
"\n"
"\n"
"\n"
"\n"

;

