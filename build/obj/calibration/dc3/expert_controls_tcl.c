

char expert_controls_tcl_proc[]=
"#!/usr/bin/wish -f\n"
"\n"
"global expert_controls\n"
"set expert_controls 0\n"
"\n"
"global tw_type xvst_type\n"
"\n"
"set tw_type 0\n"
"set xvst_type 0\n"
"\n"
"proc OpenExpertControls {} {\n"
"\n"
"	global expert_controls\n"
"	global tw_type xvst_type\n"
"	global sec sup\n"
"\n"
"	if {[expr $expert_controls!=0]} then {return}\n"
"	set expert_controls 1\n"
"\n"
"	set path .expert_controls\n"
"	toplevel $path\n"
"\n"
"	wm title $path \"DC3 expert Controls\"\n"
"	wm transient $path .\n"
"\n"
"\n"
"	button $path.dismiss -text \"Dismiss\" -command {CloseExpertControls}\n"
"	pack $path.dismiss -side bottom\n"
"\n"
"	\n"
"   ########## Timewalk function ######################################\n"
"	set win $path.timewalk\n"
"	frame $win -relief groove -borderwidth 4\n"
"	pack $win -side top -fill both -pady 5\n"
"\n"
"	set w $win.tf\n"
"	frame $w\n"
"	pack $w -side top -fill y\n"
"\n"
"	label $win.tf.lab -text \"Timewalk Function type:\"\n"
"	pack $win.tf.lab -side left\n"
"	set w $win.tf.twmenu\n"
"   menubutton $w -relief raised -text \"Timewalk Function\" -width 20 -menu $w.m\n"
"   pack $w -side left -fill both\n"
"   menu $w.m\n"
"      $w.m add radiobutton -label \"Classic(Liming)\" -variable tw_type -value 0\n"
"      $w.m add radiobutton -label \"Vipuli\"          -variable tw_type -value 1\n"
"      $w.m add radiobutton -label \"Mac\"             -variable tw_type -value 2\n"
"      $w.m add radiobutton -label \"None\"            -variable tw_type -value 3\n"
"	trace variable tw_type w {SetTWType}\n"
"	SetTWType 0 0 0\n"
"\n"
"	########### Timewalk parameter entries #################\n"
"	set w $win.bot\n"
"	frame $w\n"
"	pack $w -side top -fill both\n"
"\n"
"	for {set region 1} {[expr $region<=3]} {incr region} {\n"
"\n"
"		set rw $w.f$region\n"
"		frame $rw -relief groove -borderwidth 2\n"
"		pack $rw -side left -fill both\n"
"\n"
"		set pc $rw.l\n"
"		frame $pc\n"
"		pack $pc -side left -fill both		\n"
"		for {set p 1} {[expr $p<=5]} {incr p} {\n"
"			set pf $pc.p$p\n"
"			frame $pf\n"
"			pack $pf -side top -fill both\n"
"\n"
"			set str [format \"R%d %d\" $region $p]\n"
"			label $pf.lab -text $str\n"
"			entry $pf.en -width 8\n"
"			pack $pf.lab $pf.en -side left\n"
"		}\n"
"\n"
"		set pf $pc.fac\n"
"		frame $pf\n"
"		pack $pf -side top -fill both\n"
"		label $pf.lab -text \"fac\"\n"
"		entry $pf.en -width 8\n"
"		pack $pf.lab $pf.en -side left\n"
"\n"
"		set pf $pc.tau\n"
"		frame $pf\n"
"		pack $pf -side top -fill both\n"
"		label $pf.lab -text \"tau\" \n"
"		entry $pf.en -width 8\n"
"		pack $pf.lab $pf.en -side left\n"
"\n"
"\n"
"		set pc $rw.r\n"
"		frame $pc\n"
"		pack $pc -side left -fill both		\n"
"		for {set p 6} {[expr $p<=10]} {incr p} {\n"
"			set pf $pc.p$p\n"
"			frame $pf\n"
"			pack $pf -side top -fill both\n"
"\n"
"			set str [format \"R%d %d\" $region $p]\n"
"			label $pf.lab -text $str\n"
"			entry $pf.en -width 8\n"
"			pack $pf.lab $pf.en -side left\n"
"		}\n"
"\n"
"		set inner \"axial\" ; set outer \"stereo\"\n"
"		if {[expr $region==1]} then {set inner \"stereo\" ; set outer \"axial\"}\n"
"\n"
"		set pf $pc.betaslope_inner\n"
"		frame $pf\n"
"		pack $pf -side top -fill both\n"
"		label $pf.lab -text [format \"betaslope\n(%s)\" $inner] \n"
"		entry $pf.en -width 8\n"
"		pack $pf.lab $pf.en -side left\n"
"\n"
"		set pf $pc.betaslope_outer\n"
"		frame $pf\n"
"		pack $pf -side top -fill both\n"
"		label $pf.lab -text [format \"betaslope\n(%s)\" $outer]  \n"
"		entry $pf.en -width 8\n"
"		pack $pf.lab $pf.en -side left\n"
"		\n"
"	}\n"
"\n"
"	set w $win.but\n"
"	frame $w\n"
"	pack $w -side top -fill both\n"
"	button $w.apply -text \"Apply\" -command {ApplyExpertParameters timewalk}\n"
"	button $w.reread -text \"Reset\" -command {ResetUserParms}\n"
"	pack $w.apply $w.reread -side right\n"
"	button $w.defaults -text \"Defaults\" -command {SetParametersToDefault timewalk}\n"
"	pack $w.defaults -side left\n"
"	\n"
"	\n"
"   ########## XvsT function ######################################\n"
"	set win $path.xvst\n"
"	frame $win -relief groove -borderwidth 4\n"
"	pack $win -side top -fill both -pady 5\n"
"\n"
"	set w $win.tf\n"
"	frame $w\n"
"	pack $w -side top -fill y\n"
"\n"
"	label $win.tf.lab -text \"X vs. T Function type:\"\n"
"	pack $win.tf.lab -side left\n"
"	set w $win.tf.xvstmenu\n"
"   menubutton $w -relief raised -text \"X vs. T Function\" -width 20 -menu $w.m\n"
"   pack $w -side left -fill both\n"
"   menu $w.m\n"
"      $w.m add radiobutton -label \"Classic(Liming)\" -variable xvst_type -value 0\n"
"      $w.m add radiobutton -label \"Polynomial\"      -variable xvst_type -value 1\n"
"      $w.m add radiobutton -label \"Power\"           -variable xvst_type -value 2\n"
"      $w.m add radiobutton -label \"Bessel\"          -variable xvst_type -value 3\n"
"      $w.m add radiobutton -label \"Legendre\"        -variable xvst_type -value 4\n"
"	trace variable xvst_type w {SetXvsTType}\n"
"	SetXvsTType 0 0 0\n"
"\n"
"	########### X vs. T parameter entries #################\n"
"	set w $win.bot\n"
"	frame $w\n"
"	pack $w -side top -fill both\n"
"\n"
"	set p 1\n"
"	for {set section 1} {[expr $section<=6]} {incr section} {\n"
"\n"
"		set pc $w.f$section\n"
"		frame $pc\n"
"		pack $pc -side left -fill both		\n"
"		for {set i 1} {[expr $i<=4]} {incr i;incr p} {\n"
"			set pf $pc.p$p\n"
"			frame $pf\n"
"			pack $pf -side top -fill both\n"
"\n"
"			set str [format \"par %2d\" $p]\n"
"			label $pf.lab -text $str\n"
"			entry $pf.en -width 8\n"
"			pack $pf.lab $pf.en -side left\n"
"		}\n"
"		\n"
"	}\n"
"\n"
"	set w $win.but\n"
"	frame $w\n"
"	pack $w -side top -fill both\n"
"	button $w.apply -text \"Apply\" -command {ApplyExpertParameters xvst}\n"
"	button $w.reread -text \"Reset\" -command {ResetUserParms}\n"
"	pack $w.apply $w.reread -side right\n"
"	button $w.defaults -text \"Defaults\"  -command {SetParametersToDefault xvst}\n"
"	pack $w.defaults -side left\n"
"\n"
"	tkwait visibility $path\n"
"\n"
"	set sec $sec\n"
"}\n"
"\n"
"\n"
"proc CloseExpertControls {} {\n"
"\n"
"	global expert_controls\n"
"\n"
"	if {[expr $expert_controls==0]} then {return}\n"
"	set expert_controls 0\n"
"	destroy .expert_controls\n"
"	\n"
"}\n"
"\n"
"\n"
"proc SetTWType {name1 name2 ops} {\n"
"\n"
"	global expert_controls\n"
"	global tw_type\n"
"\n"
"	if {[expr $expert_controls==0]} then {return}\n"
"\n"
"	set str \"Unknown\"\n"
"	\n"
"	switch $tw_type {\n"
"		0 {set str \"Classic(Liming)\" }\n"
"		1 {set str \"Vipuli\" }\n"
"		2 {set str \"Mac\" }\n"
"		3 {set str \"None\" }\n"
"	}\n"
"\n"
"	.expert_controls.timewalk.tf.twmenu configure -text $str\n"
"}\n"
"\n"
"proc SetXvsTType {name1 name2 ops} {\n"
"\n"
"	global expert_controls\n"
"	global xvst_type\n"
"\n"
"	if {[expr $expert_controls==0]} then {return}\n"
"\n"
"	set str \"Unknown\"\n"
"	\n"
"	switch $xvst_type {\n"
"		0 {set str \"Classic(Liming)\" }\n"
"		1 {set str \"Polynomial\" }\n"
"		2 {set str \"Power\" }\n"
"		3 {set str \"Bessel\" }\n"
"		4 {set str \"Legendre\" }\n"
"	}\n"
"\n"
"	.expert_controls.xvst.tf.xvstmenu configure -text $str\n"
"}\n"
"\n"
"\n"
"\n"
"\n"
"\n"
"\n"

;

